#Arithmetic operators
x = 20
y = 6

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)  #Modulus - remaining after division



#Comparison operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y)
print(x != y)    #not equal to


#Assignment operators - used to assign/store values in variables
number = 45
print(number)


number += 2
print(number)

number -= 3
print(number)

#logical operators - and, or, not
num1 = 67
num2 = 43

print(num1 == num2 and num1 > num2)    #an 'and' returns a true only if all conditions are true and a false if all conditions are false
print(num1 == num2 or num1 > num2)      #returns a true if any one condition is true
print(not(num1 == num2 or num1 > num2))   #reverses your results
